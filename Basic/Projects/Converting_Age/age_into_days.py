# Prompt the user to enter their age in years
age_year = int(input("Enter your age in years: "))

# Convert the age from years to days (assuming 365 days in a year)
days = age_year * 365

# Print the result to the user
print(f"You have lived for approximately {days} days.")

# ---------------------------- Output ---------------------------- #
# Enter your age in years: 25
# You have lived for approximately 9125 days.
