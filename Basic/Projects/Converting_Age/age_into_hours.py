# Prompt the user to enter their age in years
age_years = int(input("Enter your age in years: "))

# Calculate the approximate number of hours lived
# Assuming 365 days in a year and 24 hours in a day
hours = age_years * 365 * 24

# Display the result to the user
print(f"You have lived for approximately {hours} hours.")

# ---------------------------- Output ---------------------------- #
# Enter your age in years: 25
# You have lived for approximately 219000 hours.
