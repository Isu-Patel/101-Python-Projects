# Prompt the user to enter their age in years
age_year = int(input("Enter your age in years: "))

# Convert the age from years to months
months = age_year * 12

# Display the result to the user
print(f"You have lived for approximately {months} months.")

# ---------------------------- Output ---------------------------- #
# Enter your age in years: 25
# You have lived for approximately 300 months.
